#include "libft.h"

int	ft_count_chr(const char *s, char c)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (*s)
	{
		if (*s == c)
			i++;
		s++;
	}
	if (*s == c)
		i++;
	return (i);
}
